function countProfit(shoppers) {
  let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                     ['Baju Zoro', 500000, 2],
                     ['Sweater Uniklooh', 175000, 1]
                   ];
  
  var arrOfItem = []
  if (shoppers.length === 0){
    return arrOfItem
  }else{
  
  for (var i = 0; i < listBarang.length; i++){
    var item = {}
    item.product = listBarang[i][0]
    item.shoppers = []
    item.leftOver = listBarang[i][2]
    item.totalProfit = 0
    var harga = listBarang[i][1]

     for (var j = 0; j < shoppers.length; j++){
      // console.log(shoppers[j].product)
      if (item.product === shoppers[j].product && item.leftOver >= shoppers[j].amount){
        item.shoppers.push(shoppers[j].name)
        item.leftOver = item.leftOver - shoppers[j].amount
        item.totalProfit += harga * shoppers[j].amount   
      }
        
  
        
    }
    arrOfItem.push(item)
  }
  }
  
  // console.log(arrOfItem)
 return arrOfItem
}

